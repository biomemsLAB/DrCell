function [color,line,marker]=getPlotStyle(idx)

    dark=.4;
    colors={[0 .5 1], [0 1 .5], [0 0 1], [.5-dark 1-dark 0], [1 0 1], [.5 0 1], [1 0 0], [1 .5 0], [0 1 1], [0 .5 1], [0 1 .5], [0 0 1], [.5-dark 1-dark 0], [1 0 1], [.5 0 1], [1 0 0], [1 .5 0], [0 1 1]};
    color=colors{idx};
    
    lines={'--', '-.', ':', '-', '--', '-.', ':', '-', '--', '--', '-.', ':', '-', '--', '-.', ':', '-', '--'};
    line= lines{idx};
    
    markers={'^', 's', 'x', '<', '>', 'd', '*', 'p', 'v','.','o', '^', 's', 'x', '<', '>', 'd', '*', 'p', 'v','.','o'};
    marker=markers{idx};
end